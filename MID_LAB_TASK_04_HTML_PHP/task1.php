<?php
    function Area($num1, $num2)
    {
        echo "Area of $num1 and $num2 is : ";
        echo $num2 * $num1;
    }
    Area(34, 222);

    function Perimeter($num1, $num2)
    {
        echo "Perimeter of $num1 and $num2 is : ";
        echo ($num2 + $num1) * 2;
    } 
    Perimeter(18, 350);
?>